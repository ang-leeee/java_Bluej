public class LiteralPractice
{
public static void main(String[] args)
{
    long no1= 150000l;
    float no2=200.56788f;
    char a= '\u00A9';
    System.out.println("The long variable is: "+no1);
    System.out.println("The float variable is: "+no2);
    System.out.println("The char variable is: "+a);
}
}